/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
	 //定数宣言
	  const int AD = 4096;		//AD変換数値変更用
	  const int VOLMAX = 0x1E;  //音量最大：30

	  //変数宣言
	  uint8_t volume1=0; //音量用変数
	  uint8_t volume2=0; //音量保管用変数
  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_ADC1_Init();
  MX_USART1_UART_Init();
  MX_USART2_UART_Init();
  /* USER CODE BEGIN 2 */

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
	  //AD変換開始
	  HAL_ADC_Start(&hadc1);
	  //ポーイング処理
	  if(HAL_ADC_PollForConversion(&hadc1,100)==HAL_OK)
	  {
		  analogValue = HAL_ADC_GetValue(&hadc1);
		  volume1 = analogValue*VOLMAX/AD;
	  }
	  //AD変換終了
	  HAL_ADC_Stop(&hadc1);

	  if(volume1 != volume2){
		  DFR_VOL(volume1);
	  	  volume2 = volume1;
	  }
	  //スイッチON：次の曲を再生する。
	  if(sw1 == 1){
		  DFR_NEXT();
		  sw1 = 0;
	  }
  }
  /* USER CODE END 3 */
}

